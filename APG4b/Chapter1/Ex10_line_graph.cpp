#include <bits/stdc++.h>
using namespace std;

int main() {
    int i;
    int A, B;
    cin >> A >> B;

    cout << "A:";

    i = 0;
    while (i < A) {
        cout << "]";
        i++;
    }

    cout << "\nB:";

    i = 0;
    while (i < B) {
        cout << "]";
        i++;
    }

    cout << endl;
}
